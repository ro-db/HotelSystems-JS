var hotelID,
  hotelChainID,
  numberOfRooms,
  phoneNumber,
  address,
  email,
  numberOfStars;

function HotelChain(
  hotelID,
  hotelChainID,
  numberOfRooms,
  phoneNumber,
  address,
  email,
  numberOfStars
) {
  this.hotelID = hotelID;
  this.hotelChainID = hotelChainID;
  this.numberOfRooms = numberOfRooms;
  this.email = phoneNumber;
  this.address = address;
  this.numberOfStars = numberOfRooms;
}
